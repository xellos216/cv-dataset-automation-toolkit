import argparse
import json
from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd

from utils.logging_utils import setup_logger
from utils.path_utils import LOG_DIR, STATS_DIR, ensure_output_dirs


def parse_args():
    parser = argparse.ArgumentParser(
        description="Generate summary statistics and visualizations from dataset metadata."
    )
    parser.add_argument(
        "--input-csv",
        required=True,
        help="Path to metadata CSV generated by metadata_generator.py",
    )
    parser.add_argument(
        "--output-dir",
        default=str(STATS_DIR),
        help="Directory to save summary files and plots.",
    )
    parser.add_argument(
        "--summary-json",
        default="dataset_summary.json",
        help="Filename for summary JSON inside output directory.",
    )
    parser.add_argument(
        "--log-file",
        default=str(LOG_DIR / "dataset_stats.log"),
        help="Path to save the log file.",
    )
    return parser.parse_args()


def ensure_output_dir(output_dir: Path):
    output_dir.mkdir(parents=True, exist_ok=True)


def save_histogram(series, title: str, xlabel: str, output_path: Path, bins: int = 20):
    plt.figure(figsize=(8, 5))
    plt.hist(series.dropna(), bins=bins)
    plt.title(title)
    plt.xlabel(xlabel)
    plt.ylabel("Frequency")
    plt.tight_layout()
    plt.savefig(output_path)
    plt.close()


def save_bar_chart(value_counts, title: str, xlabel: str, output_path: Path):
    plt.figure(figsize=(8, 5))
    value_counts.plot(kind="bar")
    plt.title(title)
    plt.xlabel(xlabel)
    plt.ylabel("Count")
    plt.tight_layout()
    plt.savefig(output_path)
    plt.close()


def build_summary(df: pd.DataFrame) -> dict:
    summary = {
        "total_images": int(len(df)),
        "width": {
            "min": int(df["width"].min()) if not df["width"].dropna().empty else None,
            "max": int(df["width"].max()) if not df["width"].dropna().empty else None,
            "mean": (
                round(float(df["width"].mean()), 2)
                if not df["width"].dropna().empty
                else None
            ),
        },
        "height": {
            "min": int(df["height"].min()) if not df["height"].dropna().empty else None,
            "max": int(df["height"].max()) if not df["height"].dropna().empty else None,
            "mean": (
                round(float(df["height"].mean()), 2)
                if not df["height"].dropna().empty
                else None
            ),
        },
        "file_size_bytes": {
            "min": (
                int(df["file_size_bytes"].min())
                if not df["file_size_bytes"].dropna().empty
                else None
            ),
            "max": (
                int(df["file_size_bytes"].max())
                if not df["file_size_bytes"].dropna().empty
                else None
            ),
            "mean": (
                round(float(df["file_size_bytes"].mean()), 2)
                if not df["file_size_bytes"].dropna().empty
                else None
            ),
        },
        "aspect_ratio": {
            "min": (
                round(float(df["aspect_ratio"].min()), 4)
                if not df["aspect_ratio"].dropna().empty
                else None
            ),
            "max": (
                round(float(df["aspect_ratio"].max()), 4)
                if not df["aspect_ratio"].dropna().empty
                else None
            ),
            "mean": (
                round(float(df["aspect_ratio"].mean()), 4)
                if not df["aspect_ratio"].dropna().empty
                else None
            ),
        },
        "channels_distribution": {
            str(k): int(v)
            for k, v in df["channels"].value_counts(dropna=False).to_dict().items()
        },
        "color_mode_distribution": {
            str(k): int(v)
            for k, v in df["color_mode"].value_counts(dropna=False).to_dict().items()
        },
        "readability_distribution": {
            str(k): int(v)
            for k, v in df["is_readable"].value_counts(dropna=False).to_dict().items()
        },
    }
    return summary


def main():
    args = parse_args()
    ensure_output_dirs()

    input_csv = Path(args.input_csv)
    output_dir = Path(args.output_dir)
    log_file_path = Path(args.log_file)

    ensure_output_dir(output_dir)
    log_file_path.parent.mkdir(parents=True, exist_ok=True)

    logger = setup_logger(name="dataset_stats", log_file=str(log_file_path))

    logger.info("Starting dataset statistics generation")
    logger.info(f"Input CSV: {input_csv}")
    logger.info(f"Output directory: {output_dir}")

    if not input_csv.exists():
        logger.error(f"Metadata CSV not found: {input_csv}")
        return

    df = pd.read_csv(input_csv)

    if df.empty:
        logger.error("Input metadata CSV is empty.")
        return

    logger.info(f"Rows loaded: {len(df)}")
    logger.info(f"Columns available: {list(df.columns)}")

    summary = build_summary(df)

    summary_json_path = output_dir / args.summary_json
    with open(summary_json_path, "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2, ensure_ascii=False)

    logger.info(f"Summary JSON saved to: {summary_json_path.resolve()}")

    if "width" in df.columns and not df["width"].dropna().empty:
        save_histogram(
            df["width"],
            title="Width Distribution",
            xlabel="Width (pixels)",
            output_path=output_dir / "width_distribution.png",
        )
        logger.info("Saved width distribution plot")

    if "height" in df.columns and not df["height"].dropna().empty:
        save_histogram(
            df["height"],
            title="Height Distribution",
            xlabel="Height (pixels)",
            output_path=output_dir / "height_distribution.png",
        )
        logger.info("Saved height distribution plot")

    if "aspect_ratio" in df.columns and not df["aspect_ratio"].dropna().empty:
        save_histogram(
            df["aspect_ratio"],
            title="Aspect Ratio Distribution",
            xlabel="Aspect Ratio",
            output_path=output_dir / "aspect_ratio_distribution.png",
        )
        logger.info("Saved aspect ratio distribution plot")

    if "file_size_bytes" in df.columns and not df["file_size_bytes"].dropna().empty:
        save_histogram(
            df["file_size_bytes"],
            title="File Size Distribution",
            xlabel="File Size (bytes)",
            output_path=output_dir / "file_size_distribution.png",
        )
        logger.info("Saved file size distribution plot")

    if "channels" in df.columns and not df["channels"].dropna().empty:
        channel_counts = df["channels"].value_counts().sort_index()
        save_bar_chart(
            channel_counts,
            title="Channel Distribution",
            xlabel="Channels",
            output_path=output_dir / "channel_distribution.png",
        )
        logger.info("Saved channel distribution plot")

    print("\n=== Dataset Stats Summary ===")
    print(f"Input CSV              : {input_csv}")
    print(f"Output directory       : {output_dir}")
    print(f"Total images           : {summary['total_images']}")
    print(f"Width mean             : {summary['width']['mean']}")
    print(f"Height mean            : {summary['height']['mean']}")
    print(f"Aspect ratio mean      : {summary['aspect_ratio']['mean']}")
    print(f"File size mean (bytes) : {summary['file_size_bytes']['mean']}")
    print(f"Summary JSON           : {summary_json_path}")
    print(f"Log file               : {log_file_path}")


if __name__ == "__main__":
    main()
