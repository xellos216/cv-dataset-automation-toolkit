import argparse
from pathlib import Path
from typing import Dict, List

import pandas as pd
from tqdm import tqdm

from utils.file_utils import (
    collect_files,
    compute_file_hash,
    ensure_directory,
    is_allowed_image_file,
    move_file_to_directory,
)
from utils.logging_utils import setup_logger
from utils.path_utils import LOG_DIR, REPORT_DIR, ensure_output_dirs


def parse_args():
    parser = argparse.ArgumentParser(
        description="Clean a computer vision dataset using validation reports and duplicate detection."
    )
    parser.add_argument(
        "--input",
        required=True,
        help="Path to the dataset image directory.",
    )
    parser.add_argument(
        "--report",
        default=str(REPORT_DIR / "validation_report.csv"),
        help="Path to validation report CSV generated by image_validator.py",
    )
    parser.add_argument(
        "--quarantine-dir",
        default="quarantine",
        help="Directory where invalid or duplicate files will be moved.",
    )
    parser.add_argument(
        "--remove-unsupported",
        action="store_true",
        help="Move unsupported files to quarantine.",
    )
    parser.add_argument(
        "--remove-corrupted",
        action="store_true",
        help="Move corrupted files to quarantine.",
    )
    parser.add_argument(
        "--remove-low-resolution",
        action="store_true",
        help="Move low resolution files to quarantine.",
    )
    parser.add_argument(
        "--deduplicate",
        action="store_true",
        help="Detect and move duplicate images using SHA256 hash.",
    )
    parser.add_argument(
        "--recursive",
        action="store_true",
        help="Recursively scan input directory for duplicate detection.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Preview actions without actually moving files.",
    )
    parser.add_argument(
        "--log-file",
        default=str(LOG_DIR / "dataset_cleaner.log"),
        help="Path to save the log file.",
    )
    return parser.parse_args()


def load_validation_report(report_path: Path) -> pd.DataFrame:
    if not report_path.exists():
        raise FileNotFoundError(f"Validation report not found: {report_path}")

    df = pd.read_csv(report_path)

    required_columns = {"file_path", "file_name", "is_valid", "error_reason"}
    missing_columns = required_columns - set(df.columns)

    if missing_columns:
        raise ValueError(
            f"Validation report is missing required columns: {sorted(missing_columns)}"
        )

    return df


def select_invalid_files(df: pd.DataFrame, args) -> List[Dict]:
    selections = []

    if args.remove_unsupported:
        unsupported_df = df[df["error_reason"] == "Unsupported file extension"]
        selections.extend(unsupported_df.to_dict(orient="records"))

    if args.remove_corrupted:
        corrupted_df = df[df["error_reason"] == "Unreadable or corrupted image"]
        selections.extend(corrupted_df.to_dict(orient="records"))

    if args.remove_low_resolution:
        low_res_df = df[
            df["error_reason"].fillna("").str.contains("Resolution below threshold")
        ]
        selections.extend(low_res_df.to_dict(orient="records"))

    unique_records = {}
    for record in selections:
        unique_records[record["file_path"]] = record

    return list(unique_records.values())


def quarantine_invalid_files(
    records: List[Dict], quarantine_dir: Path, dry_run: bool, logger
):
    moved_count = 0

    for record in tqdm(records, desc="Quarantining invalid files"):
        file_path = Path(record["file_path"])
        reason = record["error_reason"]

        if not file_path.exists():
            logger.warning(f"File not found, skipping: {file_path}")
            continue

        target_subdir = quarantine_dir / "invalid"

        if dry_run:
            logger.info(
                f"[DRY-RUN] Would move invalid file: {file_path} | reason={reason}"
            )
            moved_count += 1
            continue

        destination = move_file_to_directory(file_path, target_subdir)
        logger.info(
            f"Moved invalid file: {file_path} -> {destination} | reason={reason}"
        )
        moved_count += 1

    return moved_count


def find_duplicate_files(input_dir: Path, recursive: bool, logger) -> List[Path]:
    files = collect_files(str(input_dir), recursive=recursive)
    image_files = [f for f in files if is_allowed_image_file(f)]

    hash_to_original = {}
    duplicates = []

    for file_path in tqdm(image_files, desc="Detecting duplicates"):
        try:
            file_hash = compute_file_hash(file_path)
        except Exception as exc:
            logger.warning(f"Failed to hash file: {file_path} | error={exc}")
            continue

        if file_hash in hash_to_original:
            duplicates.append(file_path)
        else:
            hash_to_original[file_hash] = file_path

    return duplicates


def quarantine_duplicate_files(
    duplicates: List[Path], quarantine_dir: Path, dry_run: bool, logger
):
    moved_count = 0

    for file_path in tqdm(duplicates, desc="Quarantining duplicates"):
        target_subdir = quarantine_dir / "duplicates"

        if not file_path.exists():
            logger.warning(f"Duplicate file not found, skipping: {file_path}")
            continue

        if dry_run:
            logger.info(f"[DRY-RUN] Would move duplicate file: {file_path}")
            moved_count += 1
            continue

        destination = move_file_to_directory(file_path, target_subdir)
        logger.info(f"Moved duplicate file: {file_path} -> {destination}")
        moved_count += 1

    return moved_count


def main():
    args = parse_args()
    ensure_output_dirs()

    log_file_path = Path(args.log_file)
    log_file_path.parent.mkdir(parents=True, exist_ok=True)

    logger = setup_logger(name="dataset_cleaner", log_file=str(log_file_path))

    input_dir = Path(args.input)
    quarantine_dir = Path(args.quarantine_dir)

    logger.info("Starting dataset cleaner")
    logger.info(f"Input directory: {input_dir}")
    logger.info(f"Validation report: {args.report}")
    logger.info(f"Quarantine directory: {quarantine_dir}")
    logger.info(f"Dry run mode: {args.dry_run}")

    invalid_moved_count = 0
    duplicate_moved_count = 0

    if args.remove_unsupported or args.remove_corrupted or args.remove_low_resolution:
        try:
            df = load_validation_report(Path(args.report))
        except Exception as exc:
            logger.error(str(exc))
            return

        invalid_records = select_invalid_files(df, args)
        logger.info(f"Selected invalid files: {len(invalid_records)}")

        if not args.dry_run:
            ensure_directory(quarantine_dir / "invalid")

        invalid_moved_count = quarantine_invalid_files(
            records=invalid_records,
            quarantine_dir=quarantine_dir,
            dry_run=args.dry_run,
            logger=logger,
        )

    duplicate_count = 0
    if args.deduplicate:
        duplicates = find_duplicate_files(
            input_dir, recursive=args.recursive, logger=logger
        )
        duplicate_count = len(duplicates)
        logger.info(f"Detected duplicate files: {duplicate_count}")

        if not args.dry_run:
            ensure_directory(quarantine_dir / "duplicates")

        duplicate_moved_count = quarantine_duplicate_files(
            duplicates=duplicates,
            quarantine_dir=quarantine_dir,
            dry_run=args.dry_run,
            logger=logger,
        )

    logger.info("Dataset cleaning complete")
    logger.info(f"Invalid files moved: {invalid_moved_count}")
    logger.info(f"Duplicate files moved: {duplicate_moved_count}")

    print("\n=== Dataset Cleaner Summary ===")
    print(f"Input directory         : {input_dir}")
    print(f"Validation report       : {args.report}")
    print(f"Quarantine directory    : {quarantine_dir}")
    print(f"Dry run                 : {args.dry_run}")
    print(f"Invalid files moved     : {invalid_moved_count}")
    print(f"Duplicate files found   : {duplicate_count}")
    print(f"Duplicate files moved   : {duplicate_moved_count}")
    print(f"Log file                : {log_file_path}")


if __name__ == "__main__":
    main()
